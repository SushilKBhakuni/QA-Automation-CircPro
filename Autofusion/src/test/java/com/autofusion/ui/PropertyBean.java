package com.autofusion.ui;

public class PropertyBean {

	public String defaultBrowser;
	public String testCaseBasePath;
	public String userName;
	public String password;
	public String profile;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getProfile() {
		return profile;
	}
	public void setProfile(String profile) {
		this.profile = profile;
	}
	public String getDefaultBrowser() {
		return defaultBrowser;
	}
	public void setDefaultBrowser(String defaultBrowser) {
		this.defaultBrowser = defaultBrowser;
	}
	public String getTestCaseBasePath() {
		return testCaseBasePath;
	}
	public void setTestCaseBasePath(String testCaseBasePath) {
		this.testCaseBasePath = testCaseBasePath;
	}
	
	
	
	
}
